/*a principal diferença entre "var" e "let" é que "var" tem escopo de função ou escopo global,
e sofre de hoisting, enquanto "let" tem escopo de bloco e não sofre de hoisting. 
Em geral, recomenda-se o uso de "let" em vez de "var" na maioria das situações modernas de desenvolvimento 
JavaScript, pois "let" é mais seguro e ajuda a evitar problemas comuns associados ao escopo e hoisting.*/

